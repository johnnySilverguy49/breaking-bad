public class WalterWhite {
    private String nome;
    private int idade;
    private String carisma;
    private String inteligencia;
    private String talentoEmFazerCristal;
    private String manipulacao;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getCarisma() {
        return carisma;
    }

    public void setCarisma(String carisma) {
        this.carisma = carisma;
    }

    public String getInteligencia() {
        return inteligencia;
    }

    public void setInteligencia(String inteligencia) {
        this.inteligencia = inteligencia;
    }

    public String getTalentoEmFazerCristal() {
        return talentoEmFazerCristal;
    }

    public void setTalentoEmFazerCristal(String talentoEmFazerCristal) {
        this.talentoEmFazerCristal = talentoEmFazerCristal;
    }

    public String getManipulacao() {
        return manipulacao;
    }

    public void setManipulacao(String manipulacao) {
        this.manipulacao = manipulacao;
    }
}
